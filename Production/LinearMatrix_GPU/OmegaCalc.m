function Omega2=OmegaCalc(Omega0,Omega0ip1,Omega0im1,Omega0jp1,Omega0jm1,Ujp1,Ujm1,Vip1,Vim1,Nx,My,alpha,alphaX,alphaY,Cx2,Cy2,d,dip1,dim1,djp1,djm1)

    Omega2 = Omega0.*(1-alpha./(d.^2)) + ...
    Omega0ip1.*(-Cx2.*Ujp1.*dip1 + alphaX)./(d.^2) + ...
    Omega0im1.*( Cx2.*Ujm1.*dim1 + alphaX)./(d.^2) + ...
    Omega0jm1.*(-Cy2.*Vip1.*djp1 + alphaY)./(d.^2) + ...
    Omega0jp1.*( Cy2.*Vim1.*djm1 + alphaY)./(d.^2);
end
